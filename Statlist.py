class Statlist(object):
	"""Author: Greg Suner
		Purpose: Container class that holds a units stats. Used for encapsulation.
	"""
	def __init__(self, hp, str, magic, skill, speed, luck, defense, res):
			self.hp= hp
			self.str = str
			self.magic = magic
			self.skill = skill
			self.speed = speed
			self.luck = luck
			self.defense = defense
			self.res = res
		

