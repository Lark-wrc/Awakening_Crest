class Statlist(object):
	#__init__ stats 
	def initialiaze(hp, str, magic, skill, luck, defense, res):
			self.hp= hp
			self.str = str
			self.magic = magic
			self.skill = skill
			self.luck = luck
			self.defense = defense
			self.res = res
		

